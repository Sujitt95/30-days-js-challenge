
const add=(a,b)=>{
    return a+b
}
const sub=(a,b)=>{
    return a-b
}
const mult=(a,b)=>{
    return a*b
}
const divd=(a,b)=>{
    return a/b
}
const reman=(a,b)=>{
    return a%b
}
console.log(`5+3=${add(5,3)}`);
console.log(`5-3=${sub(5,3)}`);
console.log(`4*2=${mult(4,2)}`);
console.log(`6/3=${divd(6,3)}`);
console.log(`remainder of 5/2 is ${reman(5,2)} `);

